CREATE TABLE work_unit(
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  url TEXT NOT NULL UNIQUE,
  status TEXT NOT NULL DEFAULT 'pending',
  retry_count INTEGER NOT NULL DEFAULT 0,
  next_attempt_at TEXT NOT NULL DEFAULT to_char(timezone('UTC', now()), 'YYYY-MM-DD HH24:MI:SS'),
  last_attempt_at TEXT,
  last_error TEXT,
  created_at TEXT NOT NULL DEFAULT to_char(timezone('UTC', now()), 'YYYY-MM-DD HH24:MI:SS')
);

CREATE INDEX idx_work_unit_status_next_attempt_at ON work_unit(status, next_attempt_at);

CREATE TABLE page(
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title TEXT NOT NULL,
  url TEXT NOT NULL UNIQUE,
  links TEXT NOT NULL DEFAULT '',
  emails TEXT NOT NULL DEFAULT '',
  coins TEXT NOT NULL DEFAULT '',
  language TEXT NOT NULL DEFAULT '',
  last_scanned_at TEXT NOT NULL DEFAULT to_char(timezone('UTC', now()), 'YYYY-MM-DD HH24:MI:SS'),
  created_at TEXT NOT NULL DEFAULT to_char(timezone('UTC', now()), 'YYYY-MM-DD HH24:MI:SS')
);

CREATE TABLE page_scan(
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  page_id INTEGER NOT NULL REFERENCES page(id) ON DELETE CASCADE,
  title TEXT NOT NULL,
  language TEXT NOT NULL DEFAULT '',
  scanned_at TEXT NOT NULL DEFAULT to_char(timezone('UTC', now()), 'YYYY-MM-DD HH24:MI:SS')
);

CREATE INDEX idx_page_scan_page_id_scanned_at ON page_scan(page_id, scanned_at DESC);

CREATE TABLE page_scan_link(
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  scan_id INTEGER NOT NULL REFERENCES page_scan(id) ON DELETE CASCADE,
  target_url TEXT NOT NULL,
  target_host TEXT NOT NULL DEFAULT '',
  UNIQUE(scan_id, target_url)
);

CREATE INDEX idx_page_scan_link_target_host ON page_scan_link(target_host);

CREATE TABLE page_scan_email(
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  scan_id INTEGER NOT NULL REFERENCES page_scan(id) ON DELETE CASCADE,
  email TEXT NOT NULL,
  UNIQUE(scan_id, email)
);

CREATE INDEX idx_page_scan_email_email ON page_scan_email(email);

CREATE TABLE page_scan_crypto(
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  scan_id INTEGER NOT NULL REFERENCES page_scan(id) ON DELETE CASCADE,
  asset_type TEXT NOT NULL,
  reference TEXT NOT NULL,
  UNIQUE(scan_id, asset_type, reference)
);

CREATE INDEX idx_page_scan_crypto_lookup ON page_scan_crypto(asset_type, reference);

CREATE TABLE page_link(
