CREATE TABLE host_ssh_observation(
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  host TEXT NOT NULL,
  port INTEGER NOT NULL,
  status TEXT NOT NULL,
  host_key_algorithm TEXT,
  host_key TEXT,
  host_key_fingerprint TEXT,
  server_banner TEXT,
  last_error TEXT,
  last_attempt_at TEXT NOT NULL DEFAULT to_char(timezone('UTC', now()), 'YYYY-MM-DD HH24:MI:SS'),
