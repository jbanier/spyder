CREATE TABLE page_classification(
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  page_id INTEGER NOT NULL UNIQUE REFERENCES page(id) ON DELETE CASCADE,
  host TEXT NOT NULL,
  category TEXT NOT NULL,
  confidence TEXT NOT NULL,
  score INTEGER NOT NULL DEFAULT 0,
  evidence TEXT NOT NULL DEFAULT '',
  last_classified_at TEXT NOT NULL DEFAULT to_char(timezone('UTC', now()), 'YYYY-MM-DD HH24:MI:SS')
);

CREATE INDEX idx_page_classification_host ON page_classification(host);

CREATE TABLE site_profile(
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  host TEXT NOT NULL UNIQUE,
  category TEXT NOT NULL,
  confidence TEXT NOT NULL,
  score INTEGER NOT NULL DEFAULT 0,
  page_count INTEGER NOT NULL DEFAULT 0,
  evidence TEXT NOT NULL DEFAULT '',
  source_page_id INTEGER REFERENCES page(id) ON DELETE SET NULL,
  last_classified_at TEXT NOT NULL DEFAULT to_char(timezone('UTC', now()), 'YYYY-MM-DD HH24:MI:SS'),
